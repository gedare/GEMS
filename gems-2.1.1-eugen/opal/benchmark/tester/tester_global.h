#ifndef TESTER_GLOBAL_H
#define TESTER_GLOBAL_H

// size of the shared memory array (per processor)
extern int   g_array_size;
// number of trials to run
extern int   g_num_trials;
// seed for random number generator ... otherwise chosen by current time
extern int   g_rand_seed;
// continual causes tests to repeat forever
extern int   g_continual;
// verbosity
extern int   g_verbose;


// Min and Max functions (since they are extern inline, they are as
// fast as macros)

extern inline 
int max(int n1, int n2) 
{
  if (n1 > n2) {
    return n1;
  } else {
    return n2;
  }
}

extern inline 
int min(int n1, int n2) 
{
  if (n1 < n2) {
    return n1;
  } else {
    return n2;
  }
}

#endif //TESTER_GLOBAL_H
